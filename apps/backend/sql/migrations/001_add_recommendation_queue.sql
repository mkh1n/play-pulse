-- Initial data: create recommendation state entries for existing users
INSERT INTO public.user_recommendation_state (user_id, preferences_dirty, last_rebuild, updated_at)
SELECT 
  u.id,
  TRUE as preferences_dirty,
  NULL as last_rebuild,
  NOW() as updated_at
FROM public.users u
ON CONFLICT (user_id) DO NOTHING;
