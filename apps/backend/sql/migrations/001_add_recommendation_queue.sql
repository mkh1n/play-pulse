-- Create the user_recommendation_queue table
CREATE TABLE IF NOT EXISTS public.user_recommendation_queue (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL REFERENCES public.users(id) ON DELETE CASCADE,
  game_id BIGINT NOT NULL REFERENCES public.games(id) ON DELETE CASCADE,
  queue_position INTEGER NOT NULL,
  score NUMERIC DEFAULT 0,
  added_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  consumed BOOLEAN DEFAULT FALSE,
  consumed_at TIMESTAMP WITH TIME ZONE,
  UNIQUE(user_id, game_id)
);

-- Create indexes for performance
CREATE INDEX IF NOT EXISTS idx_user_recommendation_queue_user_id 
  ON public.user_recommendation_queue(user_id);

CREATE INDEX IF NOT EXISTS idx_user_recommendation_queue_user_consumed 
  ON public.user_recommendation_queue(user_id, consumed);

CREATE INDEX IF NOT EXISTS idx_user_recommendation_queue_position 
  ON public.user_recommendation_queue(user_id, queue_position);

-- Create index for fast lookup of next unconsumed games
CREATE INDEX IF NOT EXISTS idx_user_rec_queue_next 
  ON public.user_recommendation_queue(user_id, consumed, queue_position);

